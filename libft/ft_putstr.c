#include "../headers/libft.h"

int	ft_putstr(char *str)
{
	int	count;

	count = 0;
	if (!str)
		return (write(1, "(null)", 6));
	while (*str)
	{
		ft_putchar(*str);
		count++;
		str++;
	}
	return (count);
}
